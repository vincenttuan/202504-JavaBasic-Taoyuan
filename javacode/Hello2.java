// 簡單數學四則運算
public class Hello2 {

	// 程式進入點
	public static void main(String[] args) {
		System.out.println(5+2);     // 加法 7
		System.out.println(5-2);     // 減法 3
		System.out.println(5*2);     // 乘法 10
		System.out.println(5/2);     // 除法 2
		System.out.println(5.0/2);   // 除法 2.5
		System.out.println(5/2.0);   // 除法 2.5
		System.out.println(5.0/2.0); // 除法 2.5
		System.out.println(5%2);     // 餘數 1
		System.out.println(6/2*(1+2)); // 9
	}

}